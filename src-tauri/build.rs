use std::fs;
use std::path::Path;

fn main() {
    // Tauri on Windows strictly requires an icon.ico file to exist in the icons directory
    // for embedding into the .exe file. We dynamically generate a minimal valid 1x1 transparent ICO 
    // file here so that the build succeeds without the user having to manually download one.
    let icon_dir = Path::new("icons");
    if !icon_dir.exists() {
        fs::create_dir_all(icon_dir).unwrap();
    }
    
    let icon_path = icon_dir.join("icon.ico");
    if !icon_path.exists() {
        let minimal_ico: [u8; 70] = [
            0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x01, 0x01, 0x00, 0x00, 0x01, 0x00, 0x18, 0x00,
            0x30, 0x00, 0x00, 0x00, 0x16, 0x00, 0x00, 0x00, 0x28, 0x00, 0x00, 0x00, 0x01, 0x00,
            0x00, 0x00, 0x02, 0x00, 0x00, 0x00, 0x01, 0x00, 0x18, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x80, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x80, 0x00, 0x80, 0x00, 0x00
        ];
        fs::write(&icon_path, &minimal_ico).unwrap();
    }

    tauri_build::build()
}
